// ── Interpolation helpers ───────────────────────────────────────────

function getTideHeight(station, ms) {
  const h = (ms - TIDE_T0) / 3600000;          // fractional hour offset
  const i = Math.floor(h);
  const d = station.data;
  if (i < 0)              return d[0];
  if (i >= d.length - 1)  return d[d.length - 1];
  return d[i] + (h - i) * (d[i + 1] - d[i]);   // linear interp
}

function getTideRate(station, ms) {
  // Central-difference rate of change (ft / hr)
  const dt = 900000;                             // 15 min half-window
  return (getTideHeight(station, ms + dt) - getTideHeight(station, ms - dt))
       / (2 * dt / 3600000);
}

// ── Current vector at a world position ──────────────────────────────

function getCurrentAt(lat, lon, simMs) {
  let tw = 0, cx = 0, cy = 0;
  for (const s of TIDE_STATIONS) {
    const dlat = lat - s.lat, dlon = lon - s.lon;
    const w = 1 / (Math.sqrt(dlat * dlat + dlon * dlon) + 0.05);
    tw += w;

    const rate = getTideRate(s, simMs);
    // rate is roughly –4 … +4 ft/hr in Puget Sound
    const strength = Math.min(Math.abs(rate), 3) / 3;   // 0 … 1
    const dir = rate >= 0 ? s.floodAngle : s.floodAngle + Math.PI;

    // small spatial wobble so the field isn't perfectly uniform
    const sv = Math.sin(lat * 500 + lon * 300) * 0.2;

    cx += w * strength * Math.cos(dir + sv);
    cy += w * strength * Math.sin(dir + sv);
  }
  cx /= tw;
  cy /= tw;

  // cx, cy are in screen-space convention (x→east, y→south).
  // Convert to world degrees/tick:
  //   dlon  ∝  cx   (east  is +lon)
  //   dlat  ∝ −cy   (south is −lat)
  return {
    dlat: -cy * MAX_CURRENT,
    dlon:  cx * MAX_CURRENT / Math.cos(lat * DEG),
  };
}
